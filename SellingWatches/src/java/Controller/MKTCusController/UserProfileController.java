    /** 
     * Handles the HTTP <code>POST</code> method.
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
    throws ServletException, IOException {
        try {
            String email = request.getParameter("mail");
            String fullname = request.getParameter("uname");
            String mobile = request.getParameter("mobile");
            String address = request.getParameter("address");
            
            // update DB            
            UserDBContext userDAO = new UserDBContext();
            
            User user = userDAO.getAccountByEmail(email);          
            
            userDAO.UpdateUser(user.getUserID(), fullname, address, mobile);
            
//             Gửi phản hồi cho người dùng
            request.setAttribute("success", "Update User Profile Successfull");
            request.getRequestDispatcher("uprofile.jsp").forward(request, response);
        } catch (Exception e) {
            e.printStackTrace();            
            response.sendRedirect("uprofile.jsp?error=database_error");
            response.getWriter().print(e);
        }
    }

